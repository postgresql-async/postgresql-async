CREATE DATABASE mysql_async_tests;
USE mysql_async_tests;
CREATE TABLE mysql_async_tests.transaction_test (id VARCHAR(255) NOT NULL, PRIMARY KEY (id));
CREATE USER 'mysql_async'@'%' IDENTIFIED BY 'root';
CREATE USER 'mysql_async_sha256'@'%' IDENTIFIED WITH sha256_password; ;
SET old_passwords = 2;
SET PASSWORD FOR 'mysql_async_sha256'@'%' = PASSWORD('sha256_password');
CREATE USER 'mysql_async_nopw'@'%';
GRANT ALL PRIVILEGES ON *.* TO 'mysql_async'@'%';
GRANT ALL PRIVILEGES ON *.* TO 'mysql_async_sha256'@'%';
FLUSH PRIVILEGES;
GRANT ALL PRIVILEGES ON *.* TO 'mysql_async_nopw'@'%';
